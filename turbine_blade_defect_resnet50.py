class BladeSynthDataset(Dataset):
    def __init__(self, data_dir, split_file, transform=None):
        self.data_dir = data_dir
        self.transform = transform
        self.classes = ["Normal", "Nick", "Dent", "Scratch"]
        self.class_to_idx = {c.lower(): i for i, c in enumerate(self.classes)}

        print(f"Loading split file: {split_file}")
        self.samples = []
        missing_files = []

        with open(split_file, 'r') as f:
            for line in f:
                img_name = os.path.basename(line.strip())
                if not img_name:
                    continue

                # Add .png extension if not present
                if not os.path.splitext(img_name)[1]:
                    img_name = f"{img_name}.png"

                # Construct path relative to data_dir
                img_path = os.path.join(self.data_dir, "images", img_name)

                # Extract class from filename
                parts = os.path.splitext(img_name)[0].split('_')
                if 'valid' in parts:
                    img_class = parts[-2].lower()  # BladeSynth_valid_Dent_2133.png -> dent
                else:
                    img_class = parts[1].lower()   # BladeSynth_Dent_1126.png -> dent

                if img_class not in self.class_to_idx:
                    print(f"Warning: Unknown class '{img_class}' in {img_name}")
                    continue

                if os.path.exists(img_path):
                    self.samples.append((img_path, self.class_to_idx[img_class]))
                else:
                    missing_files.append(img_name)

        # Print class distribution
        class_counts = {}
        for _, label_idx in self.samples:
            label = self.classes[label_idx]
            class_counts[label] = class_counts.get(label, 0) + 1

        print(f"\nFound {len(self.samples)} valid images")
        print("\nClass distribution:")
        for cls, count in class_counts.items():
            print(f"{cls}: {count} images ({count/len(self.samples)*100:.1f}%)")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        img_path, label = self.samples[idx]
        image = Image.open(img_path).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, label

def main():
    # Setup paths
    COLAB_ROOT, BASELINE_DIR = setup_colab_paths()
    IMG_DIR = os.path.join(BASELINE_DIR, 'images')
    SPLITS_DIR = os.path.join(BASELINE_DIR, 'splits')

    # Configuration
    IMG_SIZE = 224  # Increased image size for better feature extraction
    BATCH = 32      # Smaller batch size for better generalization
    EPOCHS = 50     # More epochs to ensure convergence
    LR = 3e-4      # Smaller learning rate for stability
    PATIENCE = 10   # More patience to avoid early stopping
    OUT_DIR = Path(os.path.join(BASELINE_DIR, "experiments"))
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    DEVICE = "cuda" if torch.cuda.is_available() else "cpu"

    print(f"Configuration:")
    print(f"- Working directory: {COLAB_ROOT}")
    print(f"- Baseline directory: {BASELINE_DIR}")
    print(f"- Output directory: {OUT_DIR}")
    print(f"- Device: {DEVICE}")

    # Data transforms
    train_tf = transforms.Compose([
        transforms.Resize((IMG_SIZE, IMG_SIZE)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomVerticalFlip(),
        transforms.RandomRotation(15),
        transforms.ColorJitter(brightness=0.1, contrast=0.1),
        transforms.ToTensor(),
        transforms.Normalize([0.485,0.456,0.406], [0.229,0.224,0.225]),
    ])
    val_tf = transforms.Compose([
        transforms.Resize((IMG_SIZE, IMG_SIZE)),
        transforms.ToTensor(),
        transforms.Normalize([0.485,0.456,0.406], [0.229,0.224,0.225]),
    ])

    # Create datasets
    print("\nLoading datasets...")
    train_ds = BladeSynthDataset(BASELINE_DIR, os.path.join(SPLITS_DIR, "train.txt"), transform=train_tf)
    val_ds = BladeSynthDataset(BASELINE_DIR, os.path.join(SPLITS_DIR, "val.txt"), transform=val_tf)
    test_ds = BladeSynthDataset(BASELINE_DIR, os.path.join(SPLITS_DIR, "test.txt"), transform=val_tf)

    # Create dataloaders
    train_dl = DataLoader(train_ds, batch_size=BATCH, shuffle=True, num_workers=2)
    val_dl = DataLoader(val_ds, batch_size=BATCH, shuffle=False, num_workers=2)
    test_dl = DataLoader(test_ds, batch_size=BATCH, shuffle=False, num_workers=2)

    # Model
    class MLP(nn.Module):
        def __init__(self, in_dim, num_classes):
            super().__init__()
            self.net = nn.Sequential(
                nn.Flatten(),
                nn.Linear(in_dim, 1024), nn.ReLU(inplace=True), nn.BatchNorm1d(1024),
                nn.Dropout(0.3),
                nn.Linear(1024, 512), nn.ReLU(inplace=True), nn.BatchNorm1d(512),
                nn.Dropout(0.3),
                nn.Linear(512, 256), nn.ReLU(inplace=True), nn.BatchNorm1d(256),
                nn.Dropout(0.2),
                nn.Linear(256, num_classes)
            )

            # Initialize weights
            for m in self.modules():
                if isinstance(m, nn.Linear):
                    nn.init.kaiming_normal_(m.weight)
                    nn.init.constant_(m.bias, 0)

        def forward(self, x): return self.net(x)

    NUM_CLASSES = len(train_ds.classes)
    IN_DIM = 3*IMG_SIZE*IMG_SIZE
    model = MLP(IN_DIM, NUM_CLASSES).to(DEVICE)
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=LR)

    # Training
    best_val = float("inf")
    best_path = OUT_DIR / "best_mlp.pt"
    history = {"train_loss":[], "val_loss":[], "train_acc":[], "val_acc":[]}
    no_improve = 0

    def run_epoch(dl, train_mode=True):
        model.train() if train_mode else model.eval()
        tot_loss, correct, total = 0.0, 0, 0
        with torch.set_grad_enabled(train_mode):
            for x,y in dl:
                x,y = x.to(DEVICE), y.to(DEVICE)
                if train_mode: optimizer.zero_grad()
                out = model(x)
                loss = criterion(out, y)
                if train_mode:
                    loss.backward()
                    optimizer.step()
                tot_loss += loss.item()*y.size(0)
                pred = out.argmax(1)
                correct += (pred==y).sum().item()
                total += y.size(0)
        return tot_loss/total, correct/total

    for epoch in range(1, EPOCHS+1):
        tr_loss, tr_acc = run_epoch(train_dl, True)
        va_loss, va_acc = run_epoch(val_dl, False)

        history["train_loss"].append(tr_loss)
        history["val_loss"].append(va_loss)
        history["train_acc"].append(tr_acc)
        history["val_acc"].append(va_acc)

        print(f"Epoch {epoch:02d} | train {tr_acc:.3f} loss {tr_loss:.4f} | val {va_acc:.3f} loss {va_loss:.4f}")

        if va_loss < best_val:
            best_val = va_loss
            no_improve = 0
            torch.save({
                "model": model.state_dict(),
                "classes": train_ds.classes,
                "class_to_idx": train_ds.class_to_idx,
                "img_size": IMG_SIZE
            }, best_path)
        else:
            no_improve += 1
            if no_improve >= PATIENCE:
                print("Early stopping.")
                break

    # Plot learning curves
    plt.figure(figsize=(10,4))
    plt.subplot(1,2,1)
    plt.plot(history["train_acc"], label="train")
    plt.plot(history["val_acc"], label="val")
    plt.xlabel("epoch"); plt.ylabel("accuracy")
    plt.title("MLP baseline accuracy"); plt.legend()

    plt.subplot(1,2,2)
    plt.plot(history["train_loss"], label="train")
    plt.plot(history["val_loss"], label="val")
    plt.xlabel("epoch"); plt.ylabel("loss")
    plt.title("MLP baseline loss"); plt.legend()
    plt.tight_layout()
    plt.savefig(OUT_DIR/"learning_curves.png", dpi=300)

    # Test set evaluation
    ckpt = torch.load(best_path, map_location=DEVICE)
    model.load_state_dict(ckpt["model"])
    classes = ckpt["classes"]

    y_true, y_pred = [], []
    model.eval()
    with torch.no_grad():
        for x,y in test_dl:
            x = x.to(DEVICE)
            pred = model(x).argmax(1).cpu().numpy()
            y_pred.extend(pred.tolist())
            y_true.extend(y.numpy().tolist())

    print("\nClassification report (TEST):")
    print(classification_report(y_true, y_pred, target_names=classes, digits=3))

    # Confusion matrix
    cm = confusion_matrix(y_true, y_pred)
    plt.figure(figsize=(4,4))
    plt.imshow(cm, cmap="Blues")
    plt.xticks(range(len(classes)), classes, rotation=45, ha="right")
    plt.yticks(range(len(classes)), classes)
    for i in range(len(classes)):
        for j in range(len(classes)):
            plt.text(j, i, str(cm[i,j]), ha="center", va="center")
    plt.title("Confusion Matrix (Test)")
    plt.tight_layout()
    plt.savefig(OUT_DIR/"confusion_matrix.png", dpi=300)

    print(f"\nSaved outputs to: {OUT_DIR}")
    # Copy results back to Drive
    drive_out = Path("/content/drive/My Drive/APS360 - Turbine Blade Project/baseline_model/experiments")
    drive_out.mkdir(parents=True, exist_ok=True)
    shutil.copy2(OUT_DIR/"best_mlp.pt", drive_out/"best_mlp.pt")
    shutil.copy2(OUT_DIR/"learning_curves.png", drive_out/"learning_curves.png")
    shutil.copy2(OUT_DIR/"confusion_matrix.png", drive_out/"confusion_matrix.png")
    print(f"Copied results to Google Drive: {drive_out}")

