    IMG_SIZE = 224  # Increased image size for better feature extraction
    BATCH = 32      # Smaller batch size for better generalization
    EPOCHS = 6      # Reduced to 6 epochs as requested
    LR = 3e-4      # Smaller learning rate for stability
    PATIENCE = 3    # Reduced patience since we have fewer epochs
    OUT_DIR = Path(os.path.join(PRIMARY_DIR, "experiments"))
